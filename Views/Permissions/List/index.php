//[vars]----------------------------------------------------------------------------------------------------------------
$data = $parent->get_Array();
$data['permissions'] = array('singular' => false, "plural" => 'security-permissions-view-all');
$plural = $authentication->has_Permission($data['permissions']['plural']);
$submited = $request->getPost("submited");
$breadcrumb = $component . '\breadcrumb';
$validator = $component . '\validator';
$table = $component . '\table';
$deny = $component . '\deny';
$right = get_security_count_permissions();
//[build]---------------------------------------------------------------------------------------------------------------
if ($plural) {
    if (!empty($submited)) {
        $json = array(
            'breadcrumb' => view($breadcrumb, $data),
            'main' => view($validator, $data),
            'right' => "");
    } else {
        $json = array(
            'breadcrumb' => view($breadcrumb, $data),
            'main' => view($table, $data),
            'right' => $right
        );
    }
} else {
    $json = array(
        'breadcrumb' => view($breadcrumb, $data),
        'main' => view($deny, $data),
        'right' => ""
    );
}
echo(json_encode($json));
